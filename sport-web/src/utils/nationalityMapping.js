// 解析民族代码
export const parseNationalityCode = (code) => {
  // 确保code是字符串类型
  const codeStr = String(code);
  // 如果是数字编码，转换为民族名称
  if (/^\d+$/.test(codeStr)) {
    return nationalityMapping.codeToName[codeStr] || '汉族';
  }
  // 否则返回原始值
  return code;
};

// 生成民族代码
export const generateNationalityCode = (name) => {
  return nationalityMapping.nameToCode[name] || '1';
};
